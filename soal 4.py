# 4.buat program python untuk mencari nilai konversi dari celcius ke fahreinheit

celcius = float(input("Masukan nilai dalam celcius :"))
fahrenheit = (celcius * 1.8) + 32

print("============ Hasil dari konversi ==============")
print(f"Nilai dalam celcius adalah    : {celcius} derajat C")
print(f"Nilai dalam fahrenheit adalah : {fahrenheit} derajat F")